////////////////////////////////////////////////////////////////////////////////////////////////////
// CBasicShareBase
////////////////////////////////////////////////////////////////////////////////////////////////////
static CBasicShareBase *pList;
CBasicShareBase::CBasicShareBase( int _nID ): nID(_nID) 
{
	for ( CBasicShareBase *pTest = pList; pTest; pTest = pTest->pNext )
		ASSERT( pTest->nID != nID );
	pNext = pList;
	pList = this;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
// CSharedHolder
////////////////////////////////////////////////////////////////////////////////////////////////////
CSharedHolder::CSharedHolder()
{
	for ( CBasicShareBase *pTest = pList; pTest; pTest = pTest->pNext )
		pTest->CreateHolder( &objs );
}
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
void SerializeShared( CStructureSaver *pFile )
{
	for ( CBasicShareBase *pTest = pList; pTest; pTest = pTest->pNext )
		(*pTest) & ( *pFile );
}
