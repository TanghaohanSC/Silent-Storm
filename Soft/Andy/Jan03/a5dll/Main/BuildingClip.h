////////////////////////////////////////////////////////////////////////////////////////////////////
// z определяет один из 5 возможных слоев
// так же учитывается разное расположение узлов при четных\нечетных z
// x=(0..2) y=(0..2) z=(0..4)
inline DWORD GetPartBit( int x, int y, int z )
{
	if ( ( ( x + y + z ) & 1 ) == 0 )
		return 0;
	return 1 << ( x + y * 3 + (z/2) * 9 );
}
