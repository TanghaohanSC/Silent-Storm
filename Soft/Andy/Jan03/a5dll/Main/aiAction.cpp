////////////////////////////////////////////////////////////////////////////////////////////////////
// CAIAction
////////////////////////////////////////////////////////////////////////////////////////////////////
CAIAction::CAIAction( IAIState *_pState ): pState( _pState ) 
{
}
////////////////////////////////////////////////////////////////////////////////////////////////////
IAIState* CAIAction::GetState() const 
{ 
	return pState; 
}
////////////////////////////////////////////////////////////////////////////////////////////////////
IAIUnit* CAIAction::GetUnit() const 
{ 
	return pState->GetCurrentAIUnit(); 
}
////////////////////////////////////////////////////////////////////////////////////////////////////
IAIUnit* CAIAction::GetEnemy() const 
{ 
	return pState->GetCurrentAIEnemy(); 
}
////////////////////////////////////////////////////////////////////////////////////////////////////
SPlaceWithAP CAIAction::GetCurrentPlace() const
{
	SPlaceWithAP place;
	CPtr<IAIUnit> pUnit = GetUnit();
	ASSERT( pUnit );
	if ( IsValid( pUnit ) )
	{
		place.place = pUnit->GetUnitPosition();
		place.nUnitAP = pUnit->GetAP();
	}
	return place;
}
