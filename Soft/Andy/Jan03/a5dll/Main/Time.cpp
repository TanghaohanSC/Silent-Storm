#include "StdAfx.h"
#include "Time.h"
////////////////////////////////////////////////////////////////////////////////////////////////////
// CTimeCounter
////////////////////////////////////////////////////////////////////////////////////////////////////
CTimeCounter::CTimeCounter() 
{
	prevTime = 0; 
	pTime = new CCTime( 0 );
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void CTimeCounter::ResetTiming() 
{
	prevTime = 0; 
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void CTimeCounter::Advance( bool bAdvanceTime, STime currentTime )
{
	if ( bAdvanceTime )
	{
		if ( prevTime == 0 )
			prevTime = currentTime;
		STime deltaT = currentTime - prevTime;
		deltaT = Min( (STime)150, deltaT ); // min 7 fps
		pTime->Set( pTime->GetValue() + deltaT );
	}
	prevTime = currentTime;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
int CTimeCounter::operator&( CStructureSaver &f )
{
	f.Add( 1, &pTime );
	return 0;
}
