CVec4 CalcSpecularRo( float fRo )
{
	if ( fRo > 0 )
	{
		float fPower = triple( fRo ) * 256;//64;
		return CVec4( 0, 1, sqrt(sqrt( Max( fPower, 1.0f ) )), fPower );
	}
	else if ( fRo < 0 )
	{
		float fPower = triple( fabs(fRo) ) * 256;//64;
		return CVec4( 1, -1, sqrt(sqrt( Max( fPower, 1.0f ) )), fPower );
	}
	return CVec4(1,0,1,0);
}
