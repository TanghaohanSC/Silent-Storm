void NDatabase::Serialize( CDataStream &file, CStructureSaver::EMode mode )
{
	CTablesHash &tables = GetTables();
	NDatabase::bIsDatabaseLoading = true;
	{
		CStructureSaver f( file, mode );
		f.Add( 1, &tables );
	}
	NDatabase::bIsDatabaseLoading = false;
}
