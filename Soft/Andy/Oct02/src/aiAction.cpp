// CAIIteratorBasedAction
////////////////////////////////////////////////////////////////////////////////////////////////////
CAIIteratorBasedAction::CAIIteratorBasedAction( IAIState *_pAIState, IAIJob *_pParentJob ):
	CAIAction( _pAIState, _pParentJob ), bBestVersionFound(false)
{
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void CAIIteratorBasedAction::Think()
{
	ASSERT( IsValid(pAIState) );
	//
	IAIJobManager *pAIJobManager = pAIState->GetWorld()->GetAIJobManager();
	ASSERT( IsValid( pAIJobManager ) );
	//
	pAIJobManager->Remove( this );
	bBestVersionFound = false;
	bFirstIterationFinished = false;
	pAIIterator->SetMaxAP( nMaxAP );
	pAIJobManager->Add( this );
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void CAIIteratorBasedAction::DoJob()
{
	if ( !bFirstIterationFinished ) // íĺëüç˙ ďîěĺů˙ňü â Think
	{
		fExpediency = pAICriterion->GetExpediency();
		pAIIterator->First();
		bFirstIterationFinished = true;
	}
	else
	{
		float fTmpExpediency = pAICriterion->GetExpediency();
		if ( fTmpExpediency > fExpediency )
		{
			pAILog->Clear();
			pAILog->Add( pAIIterator->GetAILog() );
			fExpediency = fTmpExpediency;
		}
		pAIIterator->GetAILog()->RollBack();
		pAIIterator->GetAILog()->Clear();
		pAIIterator->Next();
	}
}
////////////////////////////////////////////////////////////////////////////////////////////////////
bool CAIIteratorBasedAction::IsJobFinished()
{
	return bFirstIterationFinished && ( pAIIterator->IsEnd() || bBestVersionFound );
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void CAIIteratorBasedAction::OnJobFinished()
{
	pAILog->Commit();
	bBestVersionFound = true;
	CAIAction::OnJobFinished();
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void CAIIteratorBasedAction::Reset()
{
	pAIIterator->Reset();
}
