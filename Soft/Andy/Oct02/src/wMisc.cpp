////////////////////////////////////////////////////////////////////////////////////////////////////
// CDParticles
////////////////////////////////////////////////////////////////////////////////////////////////////
CDParticles::CDParticles( CFuncBase<SFBTransform> *_pPlace, NDb::CEffect *_pEffect ) 
: CTimedObject( 1000 ), pPosition(_pPlace), pEffect(_pEffect)
{
}
////////////////////////////////////////////////////////////////////////////////////////////////////
CDParticles::CDParticles( const CVec3 &_pos, const CQuat &_q, NDb::CEffect *_pEffect )
: CTimedObject( 1000 ), pEffect(_pEffect)
{
  CFBMatrixStack<4> m;
  m.Init();
	m.Push( _pos, _q );
	//m.PushScale( ptScale.x, ptScale.y, ptScale.z );
	pPosition = new NGScene::CCFBTransform( m.Get() );
}
////////////////////////////////////////////////////////////////////////////////////////////////////
void CDParticles::Visit( IRenderVisitor *p )
{
	p->AddParticleEffect( GetEventTime(), pEffect, pPosition );
}
