void CVoxelCube::MovingSphereTest( int nX, int nY, float fH, int nDX, int nDY, float fDZ, SDoorColliderAnalyzer *pAn )
{
	pAn->bInClosed = pAn->bInOpen = false; pAn->pSrc = 0;
	float fX = nX, fDDX, fY = nY, fDDY, fZ = fH, fDDZ;
	int nTimes;
	if ( nDX == 0 && nDY == 0 )
	{
		int nLowerEdge1 = ( Min( fH, fH + fDZ ) - F_TEST_SPHERE_RADIUS ) / F_VOXEL_HEIGHT + 1;
		int nLowerEdge2 = Min( fH, fH + fDZ ) / F_VOXEL_HEIGHT + 1;
		int nUpperEdge1 = Max( fH, fH + fDZ ) / F_VOXEL_HEIGHT + 1;
		int nUpperEdge2 = ( Max( fH, fH + fDZ ) + F_TEST_SPHERE_RADIUS ) / F_VOXEL_HEIGHT + 1;
		for ( int z = nLowerEdge1; z < nLowerEdge2; ++z ) 
			CheckVoxel( intersectCodes[z][nY][nX], pAn );
		if ( pAn->IsCollided() )
			return;
		for ( int z = nLowerEdge2; z < nUpperEdge1; ++z ) 
		{
			CheckCircle( nX, nY, z, pAn );
			if ( pAn->IsCollided() )
				return;
		}
		for ( int z = nUpperEdge1; z < nUpperEdge2; ++z ) 
			CheckVoxel( intersectCodes[z][nY][nX], pAn );
		return; 
	}
	if ( abs(nDX) > abs(nDY) )
	{
		nTimes = abs(nDX);
		fDDX = Sign(nDX); 
		fDDY = (float)nDY / nTimes;
		fDDZ = (float)fDZ / nTimes;
	}
	else
	{
		nTimes = abs(nDY);
		fDDY = Sign(nDY); 
		fDDX = (float)nDX / nTimes;
		fDDZ = (float)fDZ / nTimes;
	}
	for ( int i = 0; i < nTimes; ++i ) 
	{
		int nLowerEdge = ( fZ - F_TEST_SPHERE_RADIUS ) / F_VOXEL_HEIGHT + 1;
		int nUpperEdge = ( fZ + F_TEST_SPHERE_RADIUS ) / F_VOXEL_HEIGHT + 1;
		int z = fZ / F_VOXEL_HEIGHT + 1;
		#define CHECK_VOX(x,y) CheckVoxel( intersectCodes[z][y+nY][x+nX], pAn );
		CHECK_VOX(-3,-1);CHECK_VOX(-3,0);CHECK_VOX(-3,1);
		CHECK_VOX(-2,-2);CHECK_VOX(-2,2);
		CHECK_VOX(-1,-3);CHECK_VOX(-1,3);
		CHECK_VOX(0,-3);CHECK_VOX(0,3);
		CHECK_VOX(1,-3);CHECK_VOX(1,3);
		CHECK_VOX(2,-2);CHECK_VOX(2,2);
		CHECK_VOX(3,-1);CHECK_VOX(3,0);CHECK_VOX(3,1);	
		#undef CHECK_VOX
		if ( pAn->IsCollided() )
			return;
		for ( z = nLowerEdge; z < nUpperEdge; ++z ) 
			CheckVoxel( intersectCodes[z][nY][nX], pAn );
		fX += fDDX;
		fY += fDDY;
		fZ += fDDZ;
		nX = fX, nY = fY;
	}
}
