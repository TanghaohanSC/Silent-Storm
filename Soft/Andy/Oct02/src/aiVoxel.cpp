void CVoxelCube::SphereTest( int nX, int nY, float fH, SDoorColliderAnalyzer *pAn )
{
	int nLowerEdge = ( fH - F_TEST_SPHERE_RADIUS ) / F_VOXEL_HEIGHT + 1;
	int nUpperEdge = ( fH + F_TEST_SPHERE_RADIUS ) / F_VOXEL_HEIGHT + 1;
	int z = fH / F_VOXEL_HEIGHT + 1;
	CheckCircle( nX, nY, z, pAn );
	if ( pAn->IsCollided() )
		return;
	for ( int z = nLowerEdge; z < nUpperEdge; ++z )
	{
		CheckVoxel( intersectCodes[z][nY][nX], pAn );
		CheckCircle( nX, nY, z, pAn );
		if ( pAn->IsCollided() )
			return;
	}
}
