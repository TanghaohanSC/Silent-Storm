void CVoxelCube::MovingSphereTest( const CVec3 &ptCenter, const CVec3 &vel, SDoorColliderAnalyzer *pAn )
{
	float fX = ptCenter.x - ptOrig.x;
	float fY = ptCenter.y - ptOrig.y;
	const float fKoef = 6 / ( FP_GRID_STEP * FP_GRID_STEP );
	int nX = (  fX * ptXDir.x + fY * ptXDir.y ) * fKoef + 8;
	int nY = ( -fX * ptXDir.y + fY * ptXDir.x ) * fKoef + 8;
	int nDX = (  vel.x * ptXDir.x + vel.y * ptXDir.y ) * fKoef;
	int nDY = (  vel.y * ptXDir.x - vel.x * ptXDir.y ) * fKoef;
	MovingSphereTest( nX, nY, ptCenter.z, nDX, nDY, vel.z, pAn );
}
