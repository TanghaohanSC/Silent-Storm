char CVoxelCube::GetObjectIntersectCode( NWorld::IVisObj *_pObject, int nTSFlags )
{
	CPtr<NWorld::IVisObj> pObject(_pObject);
	int nReturn;
	if ( !pObject )
		nReturn = 0;
	else
	{
		CObjectNumbers::iterator it = objectNumbers.find( pObject );
		if ( it != objectNumbers.end() )
			nReturn = it->second;
		else 
		{
			objectNumbers[ pObject ] = nReturn = nSpareNumber;
			if ( nTSFlags & ( NWorld::TS_STATE_OPEN | NWorld::TS_STATE_CLOSED ) )
			{
				nSpareNumber += ADC_ADD_TO_SPARE_NUMBER;
				if ( nSpareNumber > 255 )
					nSpareNumber = ADC_ADD_TO_SPARE_NUMBER;
				objects[ nReturn >> 2 ] = pObject;
			}
		}
	}
	if ( ( nTSFlags & NWorld::TS_STATE_OPEN ) == 0 )
		nReturn += ADC_CLOSED;
	if ( ( nTSFlags & NWorld::TS_STATE_CLOSED ) == 0 )
		nReturn += ADC_OPEN;
	return nReturn;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
inline void CVoxelCube::CheckVoxel( int nCode, SDoorColliderAnalyzer *pAn )
{
	if ( nCode )                                            
	{
		pAn->pSrc = objects[ nCode >> 2 ];
		if ( nCode & ADC_OPEN )
			pAn->bInOpen = true;
		if ( nCode & ADC_CLOSED )
			pAn->bInClosed = true;
	}
}
