CBaseDecorator::CBaseDecorator( IWindow *pWindow ): pThis( pWindow ), pHolder( pWindow )
{
	CPtr<IWindow> pParent = pThis->GetParent();
	if ( IsValid( pParent ) )
		pParent->DecorateChild( pThis, this );

	list<CPtr<IWindow> > childrenList;
	pThis->GetChildrenList( &childrenList );
	for ( list<CPtr<IWindow> >::iterator iTemp = childrenList.begin(); iTemp != childrenList.end(); iTemp++ )
		(*iTemp)->SetParent( this );
}
////////////////////////////////////////////////////////////////////////////////////////////////////
CBaseDecorator::~CBaseDecorator()
{
	if ( IsValid( pThis ) )
	{
		CPtr<IWindow> pParent = pThis->GetParent();
		if ( IsValid( pParent ) )
			pParent->DecorateChild( this, pThis );

		CObj<IWindow> pDestroy( pThis );
	}
}
