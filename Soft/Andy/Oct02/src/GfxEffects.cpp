void SEffConstLight::Use( CRenderContext *p )
{
	if ( IsTnLDevice() )
	{
		p->SetPixelShader( psTFactor );
		p->SetPSConst( 0, color );
	}
	else
	{
		p->SetPixelShader( psDiffuse );
		p->SetVertexShader( vsConstLight );
		p->SetVSConst( 16, color );
	}
}
