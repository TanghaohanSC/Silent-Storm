// SEffShadowBlur
////////////////////////////////////////////////////////////////////////////////////////////////////
void SEffShadowBlur::Use( CRenderContext *p )
{
	p->SetPixelShader( psShadowBlur );
	p->SetVertexShader( vsShadowBlur );
	float fOffsetX = vShift.x;
	float fOffsetY = vShift.y;
	p->SetVSConst( 16, CVec4( fScaleU, fScaleV, 0, 0 ) );
	p->SetVSConst( 17, CVec4( 0, fOffsetY, 0, 0 ) );
	p->SetVSConst( 18, CVec4(  fOffsetX, -fOffsetY, 0, 0 ) );
	p->SetVSConst( 19, CVec4( -fOffsetX, -fOffsetY, 0, 0 ) );
	p->SetTexture( 0, pTex );
	p->SetTexture( 1, pTex );
	p->SetTexture( 2, pTex );
	p->SetTexture( 3, pTex );
	p->SetPSConst( 0, CVec4( 0, 0, 0, 0.5f - 0.0f / 256 ) );
	p->SetPSConst( 1, CVec4( 0, 0, 0, 1.0f / 2 ) );
	p->SetPSConst( 2, CVec4( 0, 0, 0, 2.0f / 3 ) );
	p->SetPSConst( 3, CVec4( 0, 0, 0, 3.0f / 4 ) );
}
