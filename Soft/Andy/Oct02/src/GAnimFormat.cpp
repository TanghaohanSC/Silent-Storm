////////////////////////////////////////////////////////////////////////////////////////////////////
// CFileSkeleton
////////////////////////////////////////////////////////////////////////////////////////////////////
void CFileSkeleton::Recalc()
{
	pValue = new CFileSkeletonInfo;
	try
	{
		NGScene::CResourceOpener file( "Skeletons", GetKey() );
		file->Add( 1, &pValue->bones );
		file->Add( 2, &pValue->bScale );
	}
	catch(...)
	{
	}
}
////////////////////////////////////////////////////////////////////////////////////////////////////
const int CFileSkeletonInfo::GetBoneIndex( const char *pszName )
{
	for ( int i=0; i<bones.size(); ++i )
		if ( bones[i].szName == pszName )
			return i;
	return -1;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
// CFileAnimation
////////////////////////////////////////////////////////////////////////////////////////////////////
void CFileAnimation::Recalc()
{
	pValue = new CFileAnimationInfo;		
	try
	{
		NGScene::CResourceOpener file( "Animations", GetKey() );
		file->Add( 1, &pValue->hdr );
		file->Add( 2, &pValue->keysRoots );
		file->Add( 3, &pValue->keysBones );
		file->Add( 4, &pValue->keysAddBones );
		file->Add( 5, &pValue->keysMSR );
	}
	catch(...)
	{
	}
}
////////////////////////////////////////////////////////////////////////////////////////////////////
// CFileLocators
////////////////////////////////////////////////////////////////////////////////////////////////////
void CFileLocators::Recalc()
{
	pValue = new CFileSkeletonInfo;
	try
	{
		NGScene::CResourceOpener file( "Locators", GetKey() );
		file->Add( 1, &pValue->bones );
	}
	catch(...)
	{
	}
}
