/////////////////////////////////////////////////////////////////////////////////////
void NDatabase::AddTable( int nTableID, const char *pszTableName, 
												RecordCreateFunc newf )
{
	CTablesHash::iterator i = tables.find( nTableID );
	if ( i != tables.end() )
	{
		ASSERT(0); // already registered
		return;
	}
	ASSERT( pszTableName[ strlen( pszTableName ) - 1 ] == 's' );
	recordTypes.RegisterType( nTableID, newf );
	tables[nTableID];
}
/////////////////////////////////////////////////////////////////////////////////////
int NDatabase::GetTableID( CDBRecord *pExample )
{
	return recordTypes.GetObjectTypeID( pExample );
}
/////////////////////////////////////////////////////////////////////////////////////
CDBTable* NDatabase::GetTable( int nTableID )
{
	CTablesHash::iterator i = tables.find( nTableID );
	if ( i != tables.end() )
		return &i->second;
	return 0;
}
