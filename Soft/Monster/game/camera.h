/////////////////////////////////////////////////////////////////////////////////////
inline void Serialize( CStructureSaver *pFile, CCamera *pObj ) 
{
	pFile->AddData( 1, &pObj->pos );
	pFile->AddData( 2, &pObj->fpTangazh );
	pFile->AddData( 3, &pObj->fpRisk );
}
