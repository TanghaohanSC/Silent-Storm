// CModel
/////////////////////////////////////////////////////////////////////////////////////
void CModel::Import()
{
	NDatabase::ImportField( "ModelAnimations", this, animations );
}
/////////////////////////////////////////////////////////////////////////////////////
void CModel::Serialize( CStructureSaver *pFile ) 
{ 
	CDBRecord::Serialize( pFile );
	pFile->AddContainer( 2, &animations ); 
}
