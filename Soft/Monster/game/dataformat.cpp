/////////////////////////////////////////////////////////////////////////////////////
// main registering func
/////////////////////////////////////////////////////////////////////////////////////
void RegisterDataClasses( int nBase )
{
	REGISTER_DATABASE_CLASS( 1, "Models", CModel );
	REGISTER_DATABASE_CLASS( 2, "Animations", CAnimation );
	REGISTER_DATABASE_CLASS( 3, "Textures", CTexture );
	REGISTER_DATABASE_RELATION( "ModelAnimations" );
	REGISTER_DATABASE_RELATION( "ModelTextures" );
	//
	REGISTER_SAVELOAD_CLASS( nBase + 1, CModel );
	REGISTER_SAVELOAD_CLASS( nBase + 2, CAnimation );
	REGISTER_SAVELOAD_CLASS( nBase + 3, CTexture );
}
