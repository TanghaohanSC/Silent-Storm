/////////////////////////////////////////////////////////////////////////////////////
// CObject
/////////////////////////////////////////////////////////////////////////////////////
void CObject::Serialize( CStructureSaver *pFile )
{
	pFile->AddData( 1, &nModelID );
	pFile->AddObject( 2, &pPosition );
}
/////////////////////////////////////////////////////////////////////////////////////
// CUnit
/////////////////////////////////////////////////////////////////////////////////////
const NRPG::SUnitInfo& CUnit::GetInfo() const
{
	return pRPG->GetInfo();
}
/////////////////////////////////////////////////////////////////////////////////////
void CUnit::Serialize( CStructureSaver *pFile )
{
	CObject::Serialize( pFile );
	pFile->AddObject( 10, &pRPG );
}
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
CWorldBase* CreateWorld( NGScene::CCTime *pTime )
{
	ASSERT( pTime->IsValid() );
	CWorld *pRes = new NWorld::CWorld( pTime );
	return pRes;
}
