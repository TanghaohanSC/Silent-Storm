using namespace NWorld;
void RegisterWorldClasses( int nBase )
{
	REGISTER_SAVELOAD_CLASS( nBase + 0, CObject );
	REGISTER_SAVELOAD_CLASS( nBase + 1, CUnit );
	RegisterWorldMainClasses( nBase + 0x100000 );
}
