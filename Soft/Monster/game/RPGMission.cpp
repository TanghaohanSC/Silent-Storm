#include "StdAfx.h"
#include "RPGMission.h"
#include "RPGGlobal.h"
#include "RPGUnitInfo.h"
#include "RPGMerc.h"
/////////////////////////////////////////////////////////////////////////////////////
namespace NRPG
{
/////////////////////////////////////////////////////////////////////////////////////
// CUnitMission
/////////////////////////////////////////////////////////////////////////////////////
class CUnitMission: public CUnitMissionBase
{
	OBJECT_BASIC_METHODS(CUnitMission);
public:
	SUnitInfo info;
	//
	virtual const SUnitInfo& GetInfo() const { return info; }
	virtual void TakeDamage( int nHP );
	virtual void UpdateMerc( CMerc *pDst );
	virtual void Serialize( CStructureSaver *pFile );
};
/////////////////////////////////////////////////////////////////////////////////////
void CUnitMission::TakeDamage( int nHP )
{
	info.nHP -= nHP;
}
/////////////////////////////////////////////////////////////////////////////////////
void CUnitMission::UpdateMerc( CMerc *pDst )
{
	pDst->nExp++;
}
/////////////////////////////////////////////////////////////////////////////////////
void CUnitMission::Serialize( CStructureSaver *pFile )
{
	pFile->AddData( 1, &info );
}
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
CUnitMissionBase* CreateUnit( CMerc *pSrc )
{
	CUnitMission *pRes = new CUnitMission;
	int nHP = pSrc->nMaxHP;
	pRes->info.nHP = nHP;
	pRes->info.nMaxHP = nHP;
	return pRes;
}
/////////////////////////////////////////////////////////////////////////////////////
} // namespace
/////////////////////////////////////////////////////////////////////////////////////
using namespace NRPG;
void RegisterMissionRPGClasses( int nBase )
{
	REGISTER_SAVELOAD_CLASS( nBase + 0, CUnitMission );
}
